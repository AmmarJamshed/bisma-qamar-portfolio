/**
 * Portfolio CMS setup — creates admin user + seeds content.
 *
 * Usage (PowerShell):
 *   $env:SUPABASE_URL="https://xxxx.supabase.co"
 *   $env:SUPABASE_SERVICE_ROLE_KEY="your-service-role-key"
 *   $env:ADMIN_PASSWORD="your-chosen-password"
 *   node scripts/setup-supabase.mjs
 *
 * Never commit the service role key or password.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { createClient } from "@supabase/supabase-js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const url = process.env.SUPABASE_URL;
const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
const anonKey = process.env.SUPABASE_ANON_KEY;
const adminPassword = process.env.ADMIN_PASSWORD;
const adminEmail = "bismaqamar@portfolio.admin";

if (!url || !serviceKey || !anonKey || !adminPassword) {
  console.error(`
Missing environment variables. Set all of these:

  SUPABASE_URL              — Project Settings → API → Project URL
  SUPABASE_ANON_KEY         — Project Settings → API → anon public key
  SUPABASE_SERVICE_ROLE_KEY — Project Settings → API → service_role (secret)
  ADMIN_PASSWORD            — Password for login username "bismaqamar"

First run supabase/schema.sql in the Supabase SQL Editor.
`);
  process.exit(1);
}

const admin = createClient(url, serviceKey, {
  auth: { autoRefreshToken: false, persistSession: false },
});

const defaultContentPath = join(root, "website/js/default-content.js");
const defaultSrc = readFileSync(defaultContentPath, "utf8");
const match = defaultSrc.match(/window\.PORTFOLIO_DEFAULT_CONTENT\s*=\s*(\{[\s\S]*\});/);
if (!match) {
  console.error("Could not parse default content from", defaultContentPath);
  process.exit(1);
}
const defaultContent = Function(`return (${match[1]})`)();

async function ensureUser() {
  const { data: list } = await admin.auth.admin.listUsers({ perPage: 1000 });
  const existing = list?.users?.find((u) => u.email === adminEmail);
  if (existing) {
    await admin.auth.admin.updateUserById(existing.id, {
      password: adminPassword,
      email_confirm: true,
    });
    console.log("Updated existing admin user:", adminEmail);
    return existing.id;
  }
  const { data, error } = await admin.auth.admin.createUser({
    email: adminEmail,
    password: adminPassword,
    email_confirm: true,
  });
  if (error) throw error;
  console.log("Created admin user:", adminEmail);
  return data.user.id;
}

async function seedContent() {
  const { data: existing } = await admin.from("portfolio_content").select("id").eq("id", 1).maybeSingle();
  if (existing) {
    const { error } = await admin
      .from("portfolio_content")
      .update({ content: defaultContent, updated_at: new Date().toISOString() })
      .eq("id", 1);
    if (error) throw error;
    console.log("Updated portfolio content row.");
  } else {
    const { error } = await admin.from("portfolio_content").insert({
      id: 1,
      content: defaultContent,
    });
    if (error) throw error;
    console.log("Inserted portfolio content row.");
  }
}

function writeConfig() {
  const config = `// Auto-generated by scripts/setup-supabase.mjs — safe to commit (anon key only)
window.PORTFOLIO_CONFIG = {
  supabaseUrl: "${url}",
  supabaseAnonKey: "${anonKey}",
  adminEmailDomain: "portfolio.admin",
};
`;
  writeFileSync(join(root, "website/config.js"), config, "utf8");
  console.log("Wrote website/config.js");
}

async function main() {
  await ensureUser();
  await seedContent();
  writeConfig();
  console.log(`
Done!

Admin login:
  URL:      https://bisma-qamar-portfolio.vercel.app/admin/
  Username: bismaqamar
  Email:    ${adminEmail} (used internally by Supabase)

Next: redeploy Vercel (git push) so config.js is live.
`);
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
